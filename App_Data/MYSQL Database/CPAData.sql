-- create and select the database
DROP DATABASE IF EXISTS CPAData;
CREATE DATABASE CPAData;
USE CPAData;

-- create the tables for the database
CREATE TABLE Company(
	ID						INT				NOT NULL 	AUTO_INCREMENT,
	Company					VARCHAR(255)	NOT NULL,
	PRIMARY KEY (ID),
	INDEX Company (Company)
);

CREATE TABLE IncomeStatement (
	ID						INT				NOT NULL,
	CompanyYear				INT				NOT NULL,
	CompanyID				INT				NOT NULL,
	Company					VARCHAR(255)	NOT NULL,
	FinancialConstructs		VARCHAR(255)	NOT NULL,
	Numbers					DOUBLE			NOT NULL,
	PRIMARY KEY (ID),
	INDEX Company (Company)
);

CREATE TABLE BalanceSheet (
	ID						INT				NOT NULL,
	CompanyYear				INT				NOT NULL,
	CompanyID				INT				NOT NULL,
	Company					VARCHAR(255)	NOT NULL,
	FinancialConstructs		VARCHAR(255)	NOT NULL,
	Numbers					DOUBLE			NOT NULL,
	PRIMARY KEY (ID),
	INDEX Company (Company)
);

CREATE TABLE CommonWord (
	ID						INT				NOT NULL	AUTO_INCREMENT,
	Common					VARCHAR(30)		NOT NULL,
	PRIMARY KEY (ID),
	INDEX Common (Common)
);

CREATE TABLE KnowledgeObject(
	KnowledgeObjectID		INT				NOT NULL	AUTO_INCREMENT,
	KnowledgeObject1		VARCHAR(255)	NOT NULL,
	ObjectType				VARCHAR(255)	NOT NULL,
	PRIMARY KEY (KnowledgeObjectID),
	INDEX KnowledgeObject1 (KnowledgeObject1)
);

CREATE TABLE ISA(
	IsARelationID			INT				NOT NULL	AUTO_INCREMENT,
	KnowledgeObjectA		VARCHAR(255)	NOT NULL,
	KnowledgeObjectB		VARCHAR(255)	NOT NULL,
	PRIMARY KEY (IsARelationID),
	INDEX KnowledgeObjectA (KnowledgeObjectA)
);

CREATE TABLE ISP(
	IsPartOfRelationID		INT				NOT NULL	AUTO_INCREMENT,
	KnowledgeObjectA		VARCHAR(255)	NOT NULL,
	KnowledgeObjectB		VARCHAR(255)	NOT NULL,
	PRIMARY KEY (IsPartOfRelationID),
	INDEX KnowledgeObjectA (KnowledgeObjectA)
);

CREATE TABLE ISC(
	IsCausedByRelationID	INT				NOT NULL	AUTO_INCREMENT,
	KnowledgeObjectA		VARCHAR(255)	NOT NULL,
	KnowledgeObjectB		VARCHAR(255)	NOT NULL,
	PRIMARY KEY (IsCausedByRelationID),
	INDEX KnowledgeObjectA (KnowledgeObjectA)
);

CREATE TABLE RatioConstruct(
	RatioConstructID		INT				NOT NULL	AUTO_INCREMENT,
	RatioConstruct			VARCHAR(50)		NOT NULL,
	PRIMARY KEY(RatioConstructID),
	INDEX RatioConstruct (RatioConstruct)
);

CREATE TABLE Y_ear (
	ID						INT				NOT NULL	AUTO_INCREMENT,
	Y_EAR					VARCHAR(255)	NOT NULL,
	PRIMARY KEY (ID),
	INDEX Y_ear (Y_ear)
);

CREATE TABLE Articles(
	ArticleID				INT				NOT NULL 	AUTO_INCREMENT,
	Company					VARCHAR(255)	NOT NULL,
	TextSource				VARCHAR(255)	NOT NULL,
	ArticleURL				VARCHAR(255)	NOT NULL,
	ArticleTitle			VARCHAR(255)	NOT NULL,
	ArticleText				LONGTEXT		NOT NULL,
	PRIMARY KEY (ArticleID),
	INDEX 	Company (Company)
);

